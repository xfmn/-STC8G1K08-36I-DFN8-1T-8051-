#include "STC8G1K08.h"

void UART_Init(void)
{
    SCON = 0x50;        // 设置为模式1：8位UART，允许接收
    TMOD |= 0x20;       // 定时器1工作在模式2：8位自动重装
    TH1 = 256 - (11059200/12/32/9600); // 波特率9600
    TL1 = TH1;
    TR1 = 1;            // 启动定时器1
    ES = 1;             // 允许串口中断（可选）
    EA = 1;             // 全局中断允许（如需中断）
}
